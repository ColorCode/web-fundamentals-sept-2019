// The below code reads like this. We are going to declare a variable called i
// Then we are going to DO something until our while condition is no longer true
// The below reads in english as "We are going to increment i by one until i is no longer less than 5"

let i = 0;
do {
  i++;
  console.log("Adam has", i, "apples");
} while (i < 5);
